	/**
	 * Render icon box widget output on the frontend.
	 *
	 * Written in PHP and used to generate the final HTML.
	 */
	protected function render() {
		$settings = $this->get_settings_for_display();

		$this->add_render_attribute(
			'wrapper', 'class', [
				'farmart-team-carousel',
			]
		);

		$this->add_render_attribute(
			'wrapper', 'data-show', [
				intval($settings['slidesToShow'])
			]
		);

		$settings['infinite'] = false;

		$this->add_render_attribute( 'slicks', 'data-slick', wp_json_encode( Elementor::get_data_slick( $settings ) ) );
		$els = $settings['elements'];

		$output = [];

		if ( ! empty ( $els ) ) {
			$output[] = '<div class="row">';

			$output[] = '<div class="farmart-team-carousel--wrapper" '. $this->get_render_attribute_string( 'slicks' ).'>';
			foreach ( $els as $index => $item ) {
				$output[] = '<div class="box-member col-flex-md-3">';
				$output[] = '<div class="box-member--inner">';

				$settings['image'] = $item['image'];
				$image = Group_Control_Image_Size::get_attachment_image_html( $settings );

				if ( $item['link']['url'] ) {
					$output[] =  '<div class="image">'. $this->get_link_control( 'link-'.$index, $item['link'], $image, [ 'class' => 'has-link image-box' ] ) .'</div>';
				} else {
					$output[] =  '<div class="image"><div class="image-box">'.$image.'</div></div>';
				}

				if ( $item['name'] ) {
					if ( $item['link']['url'] ) {
						$output[] = sprintf( '<h6 class="name">%s</h6>', $this->get_link_control( 'links-'.$index, $item['link'], $item['name'], '' ) );
					} else {
						$output[] = '<h6 class="name">'.$item['name'].'</h6>';
					}
				}

				if ( $item['job'] ) {
					$output[] = sprintf( '<div class="job">%s</div>', $item['job'] );
				}

				if ( $item['desc'] ) {
					$output[] = sprintf( '<div class="desc">%s</div>', $item['desc'] );
				}
				$output[] = '</div>';
				$output[] = '</div>';

			}
			$output[] = '</div>';
			$output[] = '</div>';
		}

		?>
        <div <?php echo $this->get_render_attribute_string( 'wrapper' ); ?>>
	        <?php echo implode( '', $output ) ?>
        </div>
		<?php
	}

	protected function get_link_control( $link_key, $url, $content, $attr = [] ) {
		$attr_default = [
			'href' => $url['url'] ? $url['url'] : '#',
		];

		if ( $url['is_external'] ) {
			$attr_default['target'] = '_blank';
		}

		if ( $url['nofollow'] ) {
			$attr_default['rel'] = 'nofollow';
		}

		$attr = wp_parse_args( $attr, $attr_default );

		$this->add_render_attribute( $link_key, $attr );

		return sprintf( '<a %1$s>%2$s</a>', $this->get_render_attribute_string( $link_key ), $content );
	}

